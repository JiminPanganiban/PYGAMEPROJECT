def create_rotated_images(image_path, n):
    original_image = pygame.image.load(image_path).convert_alpha()
    angle_increment = 360 // n
    rotated_images = [pygame.transform.scale(pygame.transform.rotate(original_image, angle_increment * i),
                                             (BALL_W, BALL_H)) for i in range(n)]
    return rotated_images


def convert_ball(_character, sprite_list, jump_strength, gravity, bounce, _pop_image):
    """
    Changes the ball to different type.
    :param _character: Ball class object
    :param sprite_list: list of repeated-rotated images.
    :param jump_strength: int
    :param gravity: int
    :param bounce: int
    :param _pop_image: type of image used when ball pops
    :return: pop image to track the type of ball.
    """
    _character.sprites = sprite_list
    _character.jump_strength = jump_strength
    _character.gravity = gravity
    _character.bounce = bounce
    return _pop_image


def move_background_at_x(character, _start, frame):
    if character.change_x > 0 and character.rect.x >= SCREEN_WIDTH // 2 and (
            _start.rect.x - character.scroll_rate >= SCREEN_WIDTH - SCREEN_WIDTH * frame):
        for group in [TILE_GROUP, CANDLE_GROUP, THORN_GROUP, KEY_GROUP, PIPE_GROUP]:
            for element in group:
                element.rect.x -= character.scroll_rate
        character.rect.x -= character.scroll_rate

    elif character.change_x < 0 and character.rect.x <= SCREEN_WIDTH // 2 and (
            _start.rect.x + character.scroll_rate <= 0):
        for group in [TILE_GROUP, CANDLE_GROUP, THORN_GROUP, KEY_GROUP, PIPE_GROUP]:
            for element in group:
                element.rect.x += character.scroll_rate
        character.rect.x += character.scroll_rate


